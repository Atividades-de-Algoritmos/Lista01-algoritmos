# Faça um algoritmo de conversão de uma temperatura em graus celsius
# para uma temperatura em Farenheit.   Formula: 9/5(celsius) = farenhit – 32

# entrada de dados
celsius = float(input("informe a temperatura em celsius: "))
# processamento
farenhit = 9/5 * celsius + 32
# saída de dados
print(f"A temperatura em farenheit é: {farenhit}")
print('fim do programa')
