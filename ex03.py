# Crie um programa que recebe suas três notas (colégio, faculdade) e calcule a média final.
# calcula a média
# entrada
nota1 = int(input("informe a nota 1: "))
nota2 = int(input("informe a nota 2: "))
nota3 = int(input("informe a nota 3: "))
print(nota1 + nota2 + nota3)
# processamento
media = (nota1 + nota2 + nota3) / 3
#saida
print("a média foi ", media)
